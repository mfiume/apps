#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [[[TabController getInstance] getTab] count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    if (indexPath.row % 2 == 0) {
        cell.contentView.backgroundColor = [UIColor colorWithWhite:0.9f alpha:1.0f];
    } else {
        cell.contentView.backgroundColor = [UIColor colorWithWhite:1.0f alpha:1.0f];

    }
    
    InventoryItem* item = [[[TabController getInstance] getTab] objectAtIndex:indexPath.row];
    
    [cell.textLabel setBackgroundColor:[UIColor clearColor]];
    cell.textLabel.text = [item getName];
    
    [cell.detailTextLabel setBackgroundColor:[UIColor clearColor]];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%.2f",[item getAmount]];
    
    return cell;
}

